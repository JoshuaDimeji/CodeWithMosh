num1 = 10
num2 = 3

print('The sum of the two numbers is', num1 + num2)
print('The difference of the two numbers is', num1 - num2)
print('The product of the two numbers is', num1 * num2)
print('The quotient of the two numbers is', num1 / num2)
print('The quotient of the two numbers in whole number is', num1 // num2)
print('The remainder of the two numbers is', num1 % num2)
print('The exponent of num1 raised to num2 is', num1 ** num2)

# Argumented assignment operator, for writing same codes but in shorter forms
num2 += 3
print('The new value of the second number is', num2)



